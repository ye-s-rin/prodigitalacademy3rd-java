package day07;

/**
 * DB, 로직: 데이터 처리
 */
public class CalcModel {

    // 매개변수 정수 2개 return 합
    public int plus(int num1, int num2) {
        return num1 + num2;
    }

    public int minus(int num1, int num2) {
        return num1 - num2;
    }

    public int multi(int num1, int num2) {
        return num1 * num2;
    }

    public float divide(int num1, int num2) {
        return num1 / num2;
    }
}
